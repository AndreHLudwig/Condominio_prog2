    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        //Validar
        if (!validarInformacoes()){
            exibirMensagem("Preencha todas as informações!");
            return;
        }
        
        //Obter Dados
        String nome = tfNome.getText();
        String endereco = tfEndereco.getText();
        double taxaMensalCondominio = (double)spinTaxaMensal.getValue();
        double fatorMultiplicadorDeMetragem = (double)spinFatorMultiplicadorMetragem.getValue();
        double valorVagaGaragem = (double)spinValorVagaGaragem.getValue();
        
        //alterar
        condominioSelecionado.setNome(nome);
        condominioSelecionado.setEndereco(endereco);
        condominioSelecionado.setTaxaMensalCondominio(taxaMensalCondominio);
        condominioSelecionado.setFatorMultiplicadorDeMetragem(fatorMultiplicadorDeMetragem);
        condominioSelecionado.setValorVagaGaragem(valorVagaGaragem);
        
        //Salvar
        wrapper.condominioDAO.update(condominioSelecionado);
        
        //Exibir Mensagem
        exibirMensagem("Condomínio alterado com Sucesso!");
        
        //Limpar dados da Tela
        this.setVisible(false);
        this.telaListaDeCondominios.atualizarLista();
        this.telaListaDeCondominios.setVisible(true);
    }//GEN-LAST:event_btAlterarActionPerformed

