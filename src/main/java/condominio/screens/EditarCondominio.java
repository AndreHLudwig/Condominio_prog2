    public boolean validarInformacoes(){
        if(tfNome.getText().isBlank() || tfEndereco.getText().isBlank()){
            return false;
        } 
        return true;
    }
    
    public void iniciarDados(){
       tfNome.setText(condominioSelecionado.getNome());
       tfEndereco.setText(condominioSelecionado.getEndereco());
       spinTaxaMensal.setValue(condominioSelecionado.getTaxaMensalCondominio());
       spinFatorMultiplicadorMetragem.setValue(condominioSelecionado.getFatorMultiplicadorDeMetragem());
       spinValorVagaGaragem.setValue(condominioSelecionado.getValorVagaGaragem());
    }
